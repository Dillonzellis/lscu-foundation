          <p>The Southeastern Credit Union Foundation (SECUF) awards scholarships throughout the year for credit union CEOs, employees, board members and supervisory committee members to attend the League of Southeastern Credit Unions & Affiliates (LSCU) and Credit Union National Association (CUNA) educational events.</p>
			<p>The scholarship program is designed for smaller asset size credit unions and credit unions with financial hardships. The scholarships not only cover tuition but may also cover a portion of travel costs. Scholarships are awarded to those individuals who best exemplify the credit union philosophy, and whose attendance at approved programs would be beneficial in the perpetuation of credit union goals and ideas on an as-needed basis.</p>
			<p>Scholarship requests must be received at least 30 days before the event. Credit unions receiving SAS (Small Asset Size) funds must exhaust those funds before they are eligible to receive a scholarship.</p>
