		  <p>In times of natural disasters, concern for our credit unions and the communities they serve is a top priority for the League of Southeastern Credit Unions & Affiliates (LSCU) and the Southeastern Credit Union Foundation (SECUF). In an effort to meet the needs of our credit unions in times of disaster, the SECUF has a Disaster Relief Fund that is available to assist our credit unions, employees and volunteers that have been impacted. Grants are available to fund emergency needs, as well as, long-term and ongoing needs. Contributions and donations are accepted year-round as we never know when a disaster may strike and our goal is to be able to act immediately.</p>
			<p>Through assistance with the SECUF and the National Credit Union Foundation (NCUF), disaster relief funds can be used for a broad range of disaster-related needs as identified by credit unions in affected areas and approved by the SECUF. <strong>These needs include, but are not limited to the following items:</strong></p>
			
			<p>&bull; <strong>Critical Needs</strong> - Credit union employees and volunteers can receive assistance for items for daily living such as, but not limited to food, water, ice, batteries, clothing, diapers, temporary shelter, housing, gas and transportation.</p>
			
			<p>&bull; <strong>Longer-term Recovery Needs</strong> - After surviving the initial emergency, credit union employees and volunteers may still face long-term needs related to the disaster. Even after they recover what they can from insurance payouts, victims may need assistance rebuilding or relocating to a new home, replacing lost vehicles and household items .As long as funds remain in the SECUF Disaster Relief Fund, the Foundation in concert with its agent(s) managing the recovery efforts may expand the grant criteria to include these and other longer-term recovery needs that are not fully covered by insurance.</p>
			
			<p><em>*Credit unions having branches with significant damage and/or that have been deemed inoperable due to a natural disaster or tragic event may also be eligible to apply for disaster relieft grants and/or emergency assistance. Inquiries should be submitted via email to the SECUF Director at <a href="mailto:secuf@lscu.coop">secuf@lscu.coop</a></em><a href="mailto:secuf@lscu.coop"></a></p>
			

