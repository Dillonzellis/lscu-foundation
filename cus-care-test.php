				  <p><strong>How can my credit union accept the Credit Unions Care Challenge?</strong><br>
-	Any LSCU affiliate credit union can take the challenge.  Upon approval and commitment from the credit union’s leadership, simply fill out and <a href="cus-care-form.php">submit the online form</a>.</p>
				  
				  <p><strong>I am an Industry Partner; how can I support the movement?</strong><br>
-	To donate to the Credit Union Cares Challenge, please contact Bobbi Grady @ <a href="mailto:bobbi.grady@lscu.coop">bobbi.grady@lscu.coop</a>. </p>
				  
				  <p><strong>Will the $25 our credit unions give to our employees be taxable?</strong><br>
-	Please be advised that any money given to an employee can be viewed as additional compensation and subject to taxation.  Check with your Human Resources and Accounting departments for more information.</p>
				  
				  <p><strong>Our credit union has already taken this challenge, can we still quality for the Southeastern Credit Union Foundation match?</strong><br>
-	Yes, if your credit union has already participated in the #localstrong challenge, please fill out the submission form and the Southeastern Credit Union Foundation will match the funds up to $25 per employee.</p>
				  
				  <p><strong>Are there any limitations on how the Southeastern Credit Union Foundation matching dollars should be used?</strong><br>
-	While there are no restrictions on the Southeastern Credit Union Foundation’s matching funds, we strongly recommend that the funds are used to help others who have been negatively impacted by the current crisis.  This can include giving to a local charity, help for first responders, purchasing equipment or food for medical personnel, supplying school lunches for children, etc.  Just keep it local and impactful to your community.</p>
				  
				  <p><strong>How will a credit union receive the Southeastern Credit Union Foundation’s matching funds?</strong><br>
-	The matching funds will be sent either through paper check or an ACH payment.  A credit union can choose their preference during the online submission process.</p>
				  
				  <p><strong>How long after the online submission will a credit union receive the matching funds?</strong><br>
-	The Southeastern Credit Union Foundation will do its best to expedite delivery of the matching funds and have them available to you within 72 hours of approval of submission.</p>
				  
