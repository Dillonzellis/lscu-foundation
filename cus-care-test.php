			<p><strong>What is the #CreditUnionsCareChallenge?</strong></p>
			
			<p>The Southeastern Credit Union Foundation invites all affiliated credit unions to commit a minimum of $25 to each of their employees to do a good deed in their community by supporting a local restaurant or small business. If a credit union accepts the challenge, SECUF will then match up to $25 per employee to all participating credit unions and grant that sum amount to the credit union.  The credit union will use the matching funds to do something to make an additional impact in their community, such as:</p>
			<ul>
			  <li>•	Buy school lunches for kids</li>
			  <li>•	Purchase a meal for first responders</li>
			  <li>•	Buy equipment or food for medical personnel</li>
			  <li>•	Make a charitable contribution to a local foodbank </li>
          </ul>
			
			<p>The SECUF began this initiative to answer the challenge put forth by Tallahassee-based First Commerce Credit Union in its ambitious #LocalStrong campaign.  The credit union utilized this campaign to distribute more than $4,000 among more than 150 team members to spend with local businesses.  We were inspired to carry on First Commerce’s vision and show that we are #CUStrong by expanding it to all three of our states. Do you accept the challenge?</p>
			
			<p><strong>Credit union employees are encouraged to follow all social distancing guidelines.</strong></p>
			
