					  <h3>Biz Kid$</h3>
					  
					  <p><strong>The Show</strong><br>
					  Biz Kid$ is a national financial literacy initiative based on the Emmy Award-winning public television series where kids teach kids about money and business. The series consists of 65 episodes, each of which reaches more than one million viewers around the world. Using a clever blend of entertainment and education, each episode shows kids how to make and manage money by introducing concepts of financial literacy and entrepreneurship. To keep young viewers engaged, the series includes a fast-paced mix of direct education delivered by young actors, sketch comedies, animation, and stories featuring real-life young entrepreneurs.</p>
				  
				  <p><strong>The Educational Resource</strong><br>
					  The show is complemented by a resource-rich website, free lesson plans for teachers, outreach activities, and a monthly email newsletter. <a href="https://bizkids.com/" target="_blank">https://bizkids.com/</a>. To assist credit unions in using Biz Kid$ to connect with students, the Southeastern Credit Union Foundation created a “Biz Kid$ Showcase” competition where students (recommended for ages 10-18) can put their creative minds to the test by coming up with and developing their own business ideas!</p>
				  <p><strong>The Biz Kid$ Showcase</strong></p>
				  <p>Biz Kid$ Showcase  Competitions are a unique way for students to put their ideas into action. The Biz  Kid$ Showcase will attract and engage young members (and families) and allow  students to continue to be creative in our current virtual world. <br>
			      Beginning at the  state level, students, ages 12 – 18 years old, will complete three learning  modules, each focused on a different aspect of business planning. Each learning  module consists of a brief webinar and two Biz Kid$ episodes. After completing  each module, students will work, either as individuals or teams, to answer the  call to action. Points are received for each learning module completed. Upon  coming up with their business idea, students will then submit a business plan  and an entry into the virtual Biz Kid$ Showcase. Students have the choice of  creating a website, a short commercial video or a display to showcase their new  business idea. Entries will be judged by a panel of credit union experts from  each state (think Shark Tank style…but virtual…and a little kinder)! The top  three submissions from each state will be awarded a cash prize and move on to the  national judging for a chance at another cash prize! &nbsp; </p>
				  <p><br>
				    The Southeastern  Credit Union Foundation is excited to bring this program to you at no cost to  your credit union. We provide the toolkit to easily roll it out, we provide the  materials and webinars (all on-line in one convenient place) and we do the  tracking of the points! We even provide the prizes at the state level and the  National Credit Union Foundation provides prizes at the national level! Your  credit union&rsquo;s only commitment is to spread the word of the competition to the  communities you serve! It&rsquo;s an easy and fun way to stay connected, get  connected and provide financial education to schools, youth agencies,  homeschool groups or any other youth you may have a connection with. </p>
				  <p>Registration for the Biz Kid$ Showcase is open now through April 26, 2021 with all work to be completed by April 30, 2021.</p>
