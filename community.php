              <div class="acc_head">Community Development Financial Institution (CDFI) Program</div>
              <div class="acc_content">
				  <p>More than ever, 2020 has shown a spotlight on the incredible impact that  credit unions make in their communities.&nbsp; Whether it&rsquo;s working around the  clock to process Payment Protection Program loans, participating in social  justice initiatives or giving their employees time off to vote, credit unions  have consistently demonstrated their commitment to serving the needs of their  communities.<br>
But equitably meeting those needs in minority or low-income communities  takes commitment and innovation.&nbsp; That&rsquo;s where the U.S. Treasury  Department&rsquo;s Community Development Financial Institution (CDFI) Program can  help.<br>
Created by Congress in 1994, the CDFI program was designed to combine  federal money with private capital to help qualified financial institutions  meet the need of under-served communities that typically lack access to  affordable loans and safe financial services.<br>
&ldquo;<em>When we looked at the  needs within our community, we knew it was just too large a problem to solve on  our own.&nbsp; That&rsquo;s why we started looking into the CDFI program</em>,&rdquo;  says Kirk Mills, CEO of St. Louis Community Credit Union.&nbsp; &ldquo;<em>As an example, we wanted to offer  consumers an affordable alternative to predatory financial products and help  get our members off the payday loan merry-go-round.</em>&nbsp; <em>The benefits and grants from the CDFI  program have allowed us to focus more of our lending activities on minority and  low-income members who need it most</em>.&rdquo;</p>
				  <p><span class="text">For more information about the certification, please review the video series <a href="https://www.youtube.com/watch?v=l8ZP8lPCB9Y" target="_blank">CDFI — Introduction and Overview</a>.</span></p>
				  <p>CUNA Mutual Group, in partnership with Inclusiv and with the support of the Southeastern Credit Union Foundation, is launching a CDFI Campaign. The program aims to educate  credit unions not currently CDFI certified about the benefits of certification  in serving members and local small businesses, and to create a network for  existing CDFI credit unions to learn about and access additional resources. </p>
				  <p>As part of the awareness  and certification campaign, Inclusiv will conduct an analysis of credit unions  in target areas and provide technical assistance to credit unions interested in  pursuing CDFI certification as well as to previously CDFI-certified credit  unions whose certifications have lapsed. Inclusiv will be providing CDFI  eligibility and certification services, along with training and technical  assistance for CDFIs participating in the various CDFI Fund programs. Inclusiv  is also hosting weekly learning sessions for credit unions that would like to  learn more. </p>
				  <p><strong>Current CDFI Credit Unions are eligible to apply for a portion of the $1.25Billion allocated to CDFI by February 25th. </strong></p>
				  <p>Priority for this first round of funds is likely to be given to Minority Depository Institutions. To have this designation, your credit union must:</p>
                  <ol>
                    <li>Have a membership that is more than half minority.</li>
                    <li>Have a Board that is more than half minority. </li>
                  </ol>
                  <p>These qualifications should be self-reported by the credit union to the NCUA. More information can be found <a href="https://www.ncua.gov/support-services/credit-union-resources-expansion/resources/minority-depository-institution-preservation" target="_blank">HERE</a>. </p>
				  <p><strong>Register <a href="https://nam04.safelinks.protection.outlook.com/?url=http%3A%2F%2Fr20.rs6.net%2Ftn.jsp%3Ff%3D001jXAbHuyZNtfE3p2Y_oqLzMCS-KsvNs-4KJP9Kl0rsbq8WpkEBJ2tv15hJ5E5AKHSPqvmw2C9Kyq1e34uN3zs57IwDsWmsNNli7d0O7EmmBB2Z4O95PN3ZsfS7qVSbhhv9cCBZkFiqgwif0OoMmpPCTtxfOk-5nwey9LV9sYhpNNLldkCyusyLc_5pkXTf1axOk7U2pHAsi8CLOljgGOC9iq8JCduD7SSS2zrSiySvT7uLfX5PNvjYPP5itVF7KUxaoU3zjLDdZSo5x4qwvoTnnwn4_GbYogL_mFYYecemXI%3D%26c%3Dn2gMxQVxTOh75cAzwlXJDOSvHbL7xw3VdhT6XDaJSaS4pYQPE29gAg%3D%3D%26ch%3D74jbyC9QhWiQB9x0npJXPCwlQnaPqsNRfJugcr0InbrkCN5wWcOklQ%3D%3D&data=04%7C01%7Cjuli.lewis%40lscu.coop%7Cf00e1938e2b5421688ac08d8a1ee5366%7C35d0fbd62b1c432a96dbbeb69180cfc3%7C1%7C0%7C637437392277685123%7CUnknown%7CTWFpbGZsb3d8eyJWIjoiMC4wLjAwMDAiLCJQIjoiV2luMzIiLCJBTiI6Ik1haWwiLCJXVCI6Mn0%3D%7C1000&sdata=V0IJtj75QHAAWcHnmu9MxBO%2FC2HTkw%2BO4CVvk8Wk20I%3D&reserved=0" target="_blank">here</a> for more information and to be included in the weekly learning sessions, being held each Wednesday at 3pm EST (beginning January 6th).</strong><br>
			      </p>
				  <p><strong>Benefits of the CDFI  Program </strong><br>
				    Mission driven credit unions are dedicated to financial inclusion, and the  ability to apply for capital or technical assistance grants is often a top draw  for credit unions. &nbsp;In September 2020, the CDFI fund awarded grants  totaling $45.8M to 111 credit unions.<br>
				    Additional benefits of the CDFI program include:</p>
                  <ul type="disc">
                    <li>Access to additional training resources.</li>
                    <li>Exemption from the NCUA&rsquo;s member business lending cap.</li>
                    <li>Membership in a learning community of like-minded       credit unions to share ideas.</li>
                    <li>Limited exemptions under the CFPB Qualified Mortgage       and Ability-to-Pay rules.</li>
                  </ul>
                  <p><strong>What a CDFI Credit  Union Looks Like</strong><br>
                </p>
                  <p>Not every credit union will qualify as a CDFI.&nbsp; There are seven tests  for CDFI certification, most of which are automatic or easily fulfilled by most  credit unions.&nbsp; A good CDFI candidate makes it their primary mission to  provide development services like financial education and coaching for those  community members who need it most.&nbsp; That commitment can be seen in the  makeup of their governing or advisory boards, providing a reflection of the  communities they serve.<br>
                    The real hallmark of a CDFI credit union is that their lending activities  are predominantly focused on target populations in economically depressed  areas, low-income communities, or other historically under-served populations.<br>
  </p>
                  <p><strong>Dispelling the Myths</strong><br>
                  </p>
                  <p>It&rsquo;s a common misperception that the CDFI certification is primarily for  small credit unions.&nbsp; In fact, asset size is not a qualifying factor and  currently there are 39 CDFI certified credit unions with more than $1 billion  in assets. &nbsp;But the impact of CDFI grant dollars is magnified in a small  credit union.<br>
                    &ldquo;<em>We are a $50M credit  union with only 22-1/2 staff members</em>,&rdquo; says Kate Laud, President  and CEO of Opportunities Credit Union in Vermont. &ldquo;<em>We&rsquo;ve gotten multiple grants from the CDFI fund.&nbsp;  Those dollars have helped us run our business efficiently and cost  effectively.&nbsp; The result has been increased lending for immigrants,  minorities and low-income members in every county in Vermont.&rdquo;</em><br>
                    Because of the low mortgage rates, Opportunities Credit Union has seen  mortgage activity in 2020 equal to 270% of their 2020 plan.&nbsp; &ldquo;<em>Over a third of our mortgages in 2020  have been made to immigrants and refugees,&rdquo;</em> says Laud.<br>
                    Another common misperception is that it is just too risky to focus on  under-served communities.&nbsp; Terry Ratigan from <a href="https://nam04.safelinks.protection.outlook.com/?url=https%3A%2F%2Fwww.inclusiv.org%2Fabout-us%2F&data=04%7C01%7CJuli.Lewis%40lscu.coop%7C17e3c129e7bf4c2ed8f408d89bc03157%7C35d0fbd62b1c432a96dbbeb69180cfc3%7C1%7C0%7C637430597058211185%7CUnknown%7CTWFpbGZsb3d8eyJWIjoiMC4wLjAwMDAiLCJQIjoiV2luMzIiLCJBTiI6Ik1haWwiLCJXVCI6Mn0%3D%7C1000&sdata=8%2FnUO8sEUiWkZXp%2FP6VNUktmGWmDKJGzKOaG73m6GsU%3D&reserved=0"><strong>Inclusiv</strong></a> states that their research shows quite the opposite.<br>
                    <em>&ldquo;The median community  development credit union actually outperforms the median non-CDFI in earnings,  lending, asset growth and membership growth,&rdquo;</em> says Ratigan.&nbsp; <em>&ldquo;We have documented these results in  both our 2018 and 2020 Inclusive Finance reports.&rdquo;</em><br>
                    Comparable findings have been independently reported by NCUA and CUNA in  public presentations.<br>
                </p>
                  <p><strong>Becoming and Staying  CDFI Certified </strong><br>
                  </p>
                  <p>Some credit unions may feel they just don&rsquo;t have the time to pull together  the data and the application to initially become certified.&nbsp; That time  invested directly translates into benefits for your members.<em>&nbsp; &ldquo;You have to understand your  WHY--you don&rsquo;t become a CDFI unless it is truly part of your mission and  business strategy as a credit union.&nbsp; It takes intentional commitment  expanding who you serve and you have to be able to demonstrate your plan for  any grant dollars received from the CDFI fund,&rdquo;</em> says Mills.<br>
                    Applications for CDFI certification can be accepted any time but there are  two times a year (September and March) where the NCUA offers a streamlined  application window.&nbsp; This can significantly reduce the data gathering  burden for credit unions.<br>
                </p>
                  <p><strong>Taking Action</strong><br>
                  </p>
                  <p>As a League, we are committed to the success of credit unions.&nbsp;  Government funding for the program can change from year-to-year and there are  currently legislative proposals for unprecedented levels of funding.&nbsp; We  want to make sure all credit unions who qualify can take advantage of all CDFI  benefits.&nbsp; Inclusiv is holding weekly informational meetings for credit  unions interested in the CDFI program.&nbsp; Again, register here to learn more:<strong>&nbsp; </strong><a href="https://nam04.safelinks.protection.outlook.com/?url=https%3A%2F%2Fforms.office.com%2FPages%2FResponsePage.aspx%3Fid%3D_VIEoGmEnkCRqLt6AI4toHSFhuv9iGNHirmYo--8YMtUQ1UxVFA5U1REQTNSVUxGMUVPVUpOOU1RTS4u&data=04%7C01%7CJuli.Lewis%40lscu.coop%7C17e3c129e7bf4c2ed8f408d89bc03157%7C35d0fbd62b1c432a96dbbeb69180cfc3%7C1%7C0%7C637430597058211185%7CUnknown%7CTWFpbGZsb3d8eyJWIjoiMC4wLjAwMDAiLCJQIjoiV2luMzIiLCJBTiI6Ik1haWwiLCJXVCI6Mn0%3D%7C1000&sdata=rV7s7QqEQ9cHeZvPl25ofQRPkPWkSwrRpQvx2dGXJPA%3D&reserved=0" target="_blank"><strong>CDFI Education Sign-up</strong></a></p>
