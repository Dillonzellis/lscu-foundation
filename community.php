				  <p><strong>Join the Bank On Initiative</strong></p>
				  
				  <p><strong>The Challenge</strong><br>
Research from the Federal Deposit Insurance Corporation shows that there are many reasons consumers lack a basic transaction account: not enough money for the minimum balance; distrust of financial institutions; high or unpredictable fees; or identification, credit or banking history problems. Many Americans are driven to use expensive alternative financial services, including check cashers, payday lenders, and pawn shops that charge high fees for financial services that fail to help people get ahead. Without a checking account, families wind up paying too much for basic financial transactions and are hard pressed to build savings and assets.</p>
				  
				  <p><strong>Quick Facts:</strong></p>
				  
				  <ul style="list-style: disc;">
				  <li>Close to 7% of U.S. households (approximately 14 million adults) are “unbanked,” without a checking or savings account.</li>
					  <li>Almost 20% of U.S. households (approximately 50 million adults) are underbanked, meaning they still use some fringe financial services.</li>
					  <li>Nearly 37% of unbanked and 45% of underbanked households earn less than $30,000 per year.</li>
					  <li>Nationally, more than half of black households and 46% of Hispanic households are unbanked or underbanked, compared to less than 20% of white households.</li>
				  </ul>
				  
				  <p>Why do basic transaction accounts matter?</p>
				  
				  <p>A basic transaction account is an important first step in establishing a mainstream banking relationship, depositing earnings securely, accessing credit, and saving for the future. Unbanked and underbanked individuals lose the cost savings and stabilizing benefits such an account provides, such as:</p>
				  
				  <ul style="list-style: disc;">
				  <li>Cost savings: The average unbanked person spends 5% of net income on unnecessary fees for alternative financial services. This can amount to $40,000 over a lifetime—a significant amount for those who can least afford it.</li>
					  <li>Asset building: Without a bank account, a family lacks the ability to save reliably or automatically, or establish a banking relationship that can lead to accessing affordable credit for opportunities like a car, small business, or home mortgage.</li>
					  <li>Public safety: Without a safe place to deposit their money, unbanked people are more likely to be victims of crime because they often carry large sums of cash with them or keep cash in their homes. Elderly, disabled, or undocumented immigrants can be particularly vulnerable.</li>
					  <li>Financial stability: Research shows that being unbanked makes it harder to achieve financial goals like reducing debt and improving credit scores.</li>
				  </ul>
				  
				  <p><strong>The Solution</strong></p>
				  
				  <p><strong>The CFE (Cities for Financial Empowerment) Fund works directly with national and regional financial institutions to encourage the widespread availability of safe, low cost transactional products.</strong></p>
				  
				  <p>One key challenge facing unbanked and underbanked individuals has been a lack of safe and appropriate accounts offered by mainstream financial institutions, particularly those without overdraft.</p>
				  
				  <p>The CFE Fund has worked closely with the Bank On National Advisory Board and other key stakeholders to develop the Bank On National Account Standards. Inspired by the Federal Deposit Insurance Corporation’s Model Safe Accounts Template, these standards provide local programs with a benchmark for account partnerships with financial institutions, including their local partners.</p>
				  
