			<p><strong>What is Community Development Across the Southeast?</strong><br>
Through our Community Development Across the Southeast program, the Southeastern Credit Union Foundation has been coordinating efforts to assist credit unions in connecting with community partners, becoming low income designated and obtaining the Community Development Financial Institution (CDFI) certification and coordinating efforts to apply for federal grant money through the U.S. Treasury for community development.</p>
			<p>The SECUF currently collaborates with CU Strategic Planning and Inclusiv to assist credit unions in becoming a CDFI and in applying for potential grant funding. Credit unions are encouraged to include a pledge to the SECUF as part of the Federal grant applications submitted  to continue to provide resources in community development.</p>
			<p><strong>What are the pledged funds used for?</strong><br>
With those funds, and the help of our other partners, we provide workshops, speakers, webinars and application assistance to credit unions interested in becoming a CDFI. We also use the pledged dollars to facilitate community  discussion groups and help certified credit unions brainstorm ideas for community development within their geographic footprint, to foster assistance in connecting the credit union with community partners and new clients. The SECUF also looks for new ways that credit unions can elevate their work within the communites they serve. </p>
			
			<p><strong>Community Involvement is Key. Discover YOUR &quot;Why&quot;.</strong></p>
