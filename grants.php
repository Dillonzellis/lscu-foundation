                        PROFESSIONAL <span class="heading-accent">DEVELOPMENT GRANTS</span>
                    </h2>
                </div>
                <div class="wide-container">
                    <p class="text-primary-100 bg-primary-400 why-choose-p content-spacing">The Southeastern Credit Union Foundation (SECUF) awards professional development grants throughout the year for credit union CEOs, employees, board members and supervisory committee members to attend the League of Southeastern Credit Unions & Affiliates (LSCU) and Credit Union National Association (CUNA) educational events.<br><br>
						
						The professional development grants program is designed for smaller asset size credit unions and credit unions with financial hardships. The grants not only cover tuition or registration fee but may also cover a portion of travel costs. Professional development grants are awarded to individuals who best exemplify the credit union philosophy, and whose attendance at approved programs would be beneficial in the perpetuation of credit union goals and ideas.<br><br>

Professional development grant requests must be received at least 30 days before the event. Credit unions receiving SAS (Small Asset Size) funds must exhaust those funds before they able to receive professional development grant awards.<br>
<br>
<strong>Grant applications are accepted at any time, year-round.</strong></p>
                </div>

                <div class="accordion | container-de">
					
                    <div class="accord-col">
                        <details>
                            <summary>Grant Eligibility</summary>
							
						  <p>•	Any credit union employee or volunteer of an affiliated credit union may apply for professional development grant.<br><br>
•	Credit unions cannot be awarded the same professional development grant in consecutive years.<br><br>
•	Applicants may apply for professional development grants for different programs in different years.<br><br>
•	Individuals enrolled in a program requiring more than one year of study will be ineligible to receive an award to that program for more than one year.<br><br>
•	Scholarship requests must be received at least 30 days before the event.<br><br>
•	Credit unions under $100 million in assets must use their annual <a href="https://www.lscu.coop/member-engagement/small-cu/index.php" target="_blank">SAS initiatives account voucher</a> before applying for scholarship funds.</p>							
							
                        </details>

                        <details>
                            <summary>Grant Criteria</summary>
							
							<p>•	Based primarily, but not solely, on financial need to credit unions with a record of interest and dedication to a career in the credit union movement.<br><br>
•	Ability of credit union to pay should a grant not be awarded.<br><br>
•	Dedication of the credit union management and board to pay for the completion of an educational program which requires more than one year of study during which only one year of grant funds may be awarded.</p>							
							
                        </details>
						
						<details>
                            <summary>Grant Use & Timing of Awards</summary>
							
							<p>•	Grant awards will be used to pay tuition/registration fees and reasonable travel expenses directly related to a specific conference, workshop, or school. Funds must be used within 120 days of approval date. Any funds not used during this period will no longer be available to the recipient.<br><br>
•	Receipts must be submitted to the Foundation within 30 days of the completion of the conference, workshop, or school.<br><br>
•	Should the recipient not be able to attend a program and need to cancel after the refund deadline, reimbursement for any penalties incurred will be due to the Foundation.</p>
								
