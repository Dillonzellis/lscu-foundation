                            <p>The Community Development Grant program prioritizes funding projects that address barriers to the well-being and prosperity of credit union members and their communities, with a focus on the following funding areas:</p>
							
							<p>• <strong>Financial Wellness and Education:</strong> Projects promoting financial literacy, wholistic wellness, housing stability, and food security for credit union members and their communities. This includes initiatives such as financial coaching, healthcare access, affordable housing options, food bank support, and nutrition education, with a focusing on removing barriers to economic and social advancement. 
<br><br>
• <strong>Community Development Financial Institutions (CDFI) Certification:</strong> Projects that support credit unions seeking CDFI certification. This may include initiatives that provide technical assistance, training, or other resources to help credit unions meet the certification requirements and expand their capacity to serve underserved and low-income communities.
<br><br>
• <strong>CDFI Fund Grant Writing Assistance:</strong> Projects that support credit unions seeking CDFI Fund grants by providing funding for the hiring of grant writers. This may include initiatives that help credit unions build their grant writing capacity, such as training or technical assistance programs, as well as direct funding to hire grant writers to assist with the CDFI Fund grant application process.</p>
                        </details>
				
				<details>
