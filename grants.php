                            <p>•	The Grants Committee will review all grant applications. A report of all grant authorizations will be made to the SECUF Board of Trustees at each regularly scheduled board meeting.<br><br>
•	Approval/Denial of applications will be communicated with applicants by SECUF staff within one week of final approval by the LSCU President.<br><br>
•	Grant funds will be issued by reimbursement of incurred expenses after a detailed expense report with all receipts or invoices and a completed Grant Impact Report form (with all required documentation) is submitted to the SECUF staff. The report and documentation must be submitted within 30 days upon completion of the project.<br><br>
•	Applicants may be awarded partial funding to begin their proposed project, subject to approval. To request partial funding, applicants must submit a written explanation of the need for such funding to initiate the project.<br><br>
•	Credit Unions are required to notify all credit union staff and board of the awarded grant via email and copy SECUF staff.<br><br>
•	Recipient must use the grant during the calendar year it is awarded unless otherwise specified in request. Grant funds provided are only for the purpose stated on the application. Funds spent on other training, projects, or events may not be reimbursed. If grant funds are unused in this calendar year, funds may be re-claimed for future use.</p>
