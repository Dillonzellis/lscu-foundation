			<h4 align="center"><strong>The Credit Unions Care Challenge was executed April-September 2020. Thank you to all who made this program a huge success! See below for the overall impact made!</strong></h4>
			
			
			<p><strong>What was the #CreditUnionsCareChallenge?</strong></p>
			
			<p><strong>Credit unions throughout Alabama, Florida and Georgia sparked hope in local communities by participating in the Credit Union Cares Challenge, putting over $1Million back into our local businesses during the COVID19 pandemic!</strong></p>
			<p>The Southeastern Credit Union Foundation invited all affiliated credit unions to commit a minimum of $25 to each of their employees to do a good deed in their community by supporting a local restaurant or small business. If a credit union accepted the challenge, SECUF then matched up to $25 per employee to all participating credit unions and granted that sum amount to each credit union. The credit unions then used the matching funds to do something to make an additional impact in their community.</p>
			
