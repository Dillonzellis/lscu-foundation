              <div class="acc_head">How CDA Works</div>
              <div class="acc_content">
				  <p>A Charitable Donation Account (“CDA”) investment allows a credit union to make a charitable contribution while allowing the contribution to pay for itself. Over the last 3-5 years, an investment in the CDA would have allowed a credit union to contribute five times more on their charitable donations and earn back five times more than traditional investments.</p>
				  <p><strong>What is a Charitable Donation Account Investment?</strong></p>
				  <p>CDA is a hybrid investment which grants a  credit union expanded investment powers to fund charitable contributions. To qualify as a CDA, the primary purpose of the investment must be to fund charitable contributions. To meet the primary purpose test, a minimum of 51 percent of the earnings and capital gains must be distributed to at least one designated 501(c)3 at a frequency of no less than five years (annual distributions are preferred). Earnings may be split between multiple chaitable organzations. Gains and interest in excess of the 51 percent are booked as investment income by the credit union.</p>
				  <p><strong>How are contributions distributed (51 percent of investment return)?</strong></p>
				  <p>Contributions from the CDA will be shared between charitable organizations as designated by the credit union. The Southeastern Credit Union Foundation encourages credit unions to name the SECUF as your charitable organization of choice to enable the SECUF to continue to offer free resources, grants and innovative programs. </p>
				  <p><strong>How often are contributions made  from the CDA?</strong></p>
				  <p>Contributions will be made annually.</p>

