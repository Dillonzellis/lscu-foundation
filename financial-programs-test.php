				  <p><strong>The Show</strong><br>
					  Biz Kid$ is a national financial literacy initiative based on the Emmy Award-winning public television series where kids teach kids about money and business. The series consists of 65 episodes, each of which reaches more than one million viewers around the world. Using a clever blend of entertainment and education, each episode shows kids how to make and manage money by introducing concepts of financial literacy and entrepreneurship. To keep young viewers engaged, the series includes a fast-paced mix of direct education delivered by young actors, sketch comedies, animation, and stories featuring real-life young entrepreneurs.</p>
				  
				  <p><strong>Educational Resource</strong><br>
					  The show is complemented by a resource-rich website, free lesson plans for teachers, outreach activities, and a monthly email newsletter. <a href="https://bizkids.com/" target="_blank">https://bizkids.com/</a>. To assist credit unions in using Biz Kids to connect with students, the Southeastern Credit Union Foundation created a “Biz Kids Showcase” competition where students (recommended for ages 10-18) can put their creative minds to the test by coming up with and developing their own business ideas!</p>
				  
				  <p>The Southeastern Credit Union Foundation’s <a href="docs/Bizkids-COVID19-Alternative-Plan.pdf" target="_blank">Biz Kids Showcase</a> also took on a new format half-way through, showing the adaptability, creative side and dedication of the SECUF. The SECUF began with workshops in Tuscaloosa, Birmingham and Tampa, hosting approximately 200 students ages 10-18. The students were introduced to entrepreneurial activities and knowledge from the Biz Kids TV series and asked to come up with a business plan, either independently or working in a group. The group would then come back together in April for a showcase where “investors” could hear their business ideas and vote on those they thought had the most promise. However, at the onset of the COVID19 Pandemic, the SECUF immediately came up with an alternative plan where participants could earn points for attending up to 4 webinars, submitting their business plan on time and creating a website to showcase their business online. Investors then judged the websites and came up with an overall winner based on points accrued, business idea and website.  Jada W from Birmingham Alabama, sponsored by Alabama One CU was named the Biz Kids Champion. She received $500 and an invitation from the University of Alabama at Birmingham to intern with their football team as their sports photographer. The future is bright for this rising high school senior!</p>
				  
