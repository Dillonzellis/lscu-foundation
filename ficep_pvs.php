          <p>The LSCU and the Southeastern Credit Union Foundation (SECUF) partner with CUNA to offer the unique financial counseling training program for credit union staff and enhanced Financial Counseling Certification Program (FiCEP). The program combines the affordability and convenience of a self-study program with the group hands-on learning and networking of an off-site CUNA School.</p>
			<p>Participants have access to recorded webinars for each module to help them stay on track. In-person group training sessions are held in Alabama, Florida and Georgia for Part I & Part II providing hands-on training with experienced counselors, as well as the opportunity to network, share best practices and prepare for exams. Participants will be able to test as a group or individually.</p>
			<p>Individuals who successfully complete all eight modules (divided into two parts of 4 modules each) and exams earn their Credit Union Certified Financial Counselor (CUCFC) designation.</p>
			
