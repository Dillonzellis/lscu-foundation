			<p>The Southeastern Credit Union Disaster Relief Fund provides grants to credit union employees and volunteers who have been affected by significant natural disasters. These grants serve the primary purpose of offering financial support to individuals who have encountered unforeseen expenses as a result of the disaster's impact. The objective of the funding is to ensure that the essential needs for survival are adequately met.</p>
			
			<p>The allocated funds are intended to address various aspects that have been negatively affected by the disaster. These include repairing damage and covering expenses related to housing, maintaining safe living environments, and ensuring access to fundamental necessities to sustain livelihoods in the aftermath of the natural disaster.</p>
			
		  <p>It's important to note the following aspects about the grants:</p>
			
		  <p><strong>•	Scope of Assistance:</strong> The grants do not serve as a replacement for assistance provided by FEMA, insurance policies, or any other forms of federal or non-profit aid.</p>
			<p><strong>•	Exclusions:</strong> The grants do not cover luxury items, such as outdoor decorations, landscaping, plants, patio furniture, fences, barns, sheds, or any non-essential/critical items that are not directly related to survival and basic needs.</p>
			<p><strong>•	Tree-Related Damages:</strong> Damages caused by trees, tree removal, or expenses resulting from fallen trees are typically not considered for grant approval. Exceptions are made if a fallen tree poses a threat to a dwelling or obstructs access to vital resources like power or water.</p>
			
